# Basic Program To Find Average of Two Numbers Given By User

# User input based variables
a = input("Enter the First Number : ")  
b = input("Enter the Second Number : ")

# Converting string based input into an integer data type
a = int(a)
b = int(b)

# Creating an Average Function Variable
Average = (a+b)/2

# Printing the average result
print("The average of the two given numbers is : ", Average)
