# PROGRAM TO GREET ALL THE PERSONS IN THE LIST WHOSE NAME STARTS WITH S-LETTER

# List of names
l = ['harsh','sonaal','ritu','shubham','sharvesh','monty','mahesh','shyam','shikha',
     'trupti','veena','sita']

# for-looping
for name in l:
    if name.startswith('s'):
        print('Hello, goodmorning '+name)

